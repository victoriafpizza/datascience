# lista gerada

lista = [16, 14, 63, 65, 17, 99, 70, 11, 20, 48, 79, 32, 17, 25, 66]

# lendo o tamanho, maior e menor numero e soma, usando as built-in function 
tam = len(lista)
maior = max(lista)
menor = min(lista)
soma = sum(lista)

print(f"A lista possui {tam} numeros em que o maior numero é {maior} e o menor é {menor}. A soma dos valores é {soma}")